let _currentLang = localStorage.getItem(STORAGE_KEY) || 'uk'

export function getLang()  { return _currentLang }

export function setLang(lang) {
  if (!TRANSLATIONS[lang]) return
  _currentLang = lang
  localStorage.setItem(STORAGE_KEY, lang)
  document.documentElement.lang = lang
  window.dispatchEvent(new CustomEvent('lang-change', { detail: { lang } }))
}

/** Translate key. Falls back to Ukrainian, then to the key itself. */
export function t(key, vars = {}) {
  let str = TRANSLATIONS[_currentLang]?.[key]
           ?? TRANSLATIONS['uk']?.[key]
           ?? key
  // Simple variable substitution: t('hello', { name: 'Ivan' }) → 'Привіт, Ivan'
  for (const [k, v] of Object.entries(vars)) {
    str = str.replace(new RegExp(`\\{${k}\\}`, 'g'), v)
  }
  return str
}

